<template>
  <div  class="tab-control">
    <div v-for="(item,index) in titles"
    :class="{active:index===currentIndex}"
     @click="itemClick(index)"
    >
      <span>{{item}}</span>
    </div>

  </div>

</template>

<script>
export default {
  name: "TabControl",
  data(){
    return{
      currentIndex:0
    }
  },
  methods:{
    itemClick(index){
      this.currentIndex=index
      this.$emit('tabClick',index)
    }
  },
  props:{
    titles:{
      type:Array,
      default(){
        return []
      }
    }
  }
}
</script>

